package com.mx.petcare.auth.security;

import io.jsonwebtoken.*;
import io.jsonwebtoken.security.Keys;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

import java.security.Key;
import java.util.Date;
import java.util.HashMap;
import java.util.Map;

@Component
public class JwtUtil {

    private static final Logger logger = LoggerFactory.getLogger(JwtUtil.class);

    @Value("${jwt.secret}")
    private String secret;

    @Value("${jwt.expiration}")
    private long expiration;

    public String generateToken(String username, Long idUser, String role) {
        logger.info("generateToken :: {}", username);
        Map<String, Object> claims = new HashMap<>();
        claims.put("id", idUser);
        claims.put("username", username);
        claims.put("role", role);

        Key key = Keys.hmacShaKeyFor(secret.getBytes());

        logger.debug("generateToken KEY algorithm: {}, format: {}",
                key.getAlgorithm(), key.getFormat());

        try {
            String token = Jwts.builder()
                    .setClaims(claims)
                    .setSubject(username)
                    .setIssuedAt(new Date(System.currentTimeMillis()))
                    .setExpiration(new Date(System.currentTimeMillis() + expiration))
                    .signWith(key)
                    .compact();

            logger.debug("generateToken TOKEN : {}", token);

            return token;
        } catch (Exception e) {
            logger.error("Error generating token", e);
            throw new RuntimeException("Error generando token JWT");
        }
    }

    public String extractUsername(String token) {
        return Jwts.parserBuilder()
                .setSigningKey(secret.getBytes())
                .build()
                .parseClaimsJws(token)
                .getBody()
                .getSubject();
    }

    public boolean validateToken(String token) {
        try {
            Jwts.parserBuilder().setSigningKey(secret.getBytes()).build().parseClaimsJws(token);
            return true;
        } catch (JwtException e) {
            return false;
        }
    }
}
